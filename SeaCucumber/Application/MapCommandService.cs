        public static int DetermineFirstStepWithoutMovement(Map map)
        {
            ConsiderStepEast(map);
            int seaCucumbersCanStep = SetStep(map);

            ConsiderStepSouth(map);
            seaCucumbersCanStep += SetStep(map);

            int nrOfSteps = 1;
            while (seaCucumbersCanStep > 0)
            {
                nrOfSteps++;

                ConsiderStepEast(map);
                seaCucumbersCanStep = SetStep(map);

                ConsiderStepSouth(map);
                seaCucumbersCanStep += SetStep(map);
            }

            return nrOfSteps;
        }

        private static void ConsiderStepEast(Map map)
        {
            map.ConsiderStepEast();
        }

        private static void ConsiderStepSouth(Map map)
        {
            map.ConsiderStepSouth();
        }

        private static int SetStep(Map map)
        {
            return map.SetStep();
        }
